#ARITHMETIC OPERATIONS

print(10 + 3) #addition
print(10 - 3) #subtraction
print(10 * 3) #multiplication
print(10 / 3) #division
print(10 // 3) #rounds down to the nearest integer
print(10 % 3) #modulus - returns the remainder of the division
print(10 ** 3) #power

x = 10
x = x + 3
print(x)

x = 10
x += 3 #same as x = x + 3
print (x)

x = 10
x -= 3
print(x)


#Operator precedence:

x = 10 + 3 * 2 #follows proper order of operations i.e., BODMAS
print(x)

y = 3 ** 2 #3 to the power of 2
print(y)

z = (10 + 3) * 2 - 4
print(z)