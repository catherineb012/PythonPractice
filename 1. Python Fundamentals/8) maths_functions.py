#MATH FUNCTIONS

x = 2.9
print(round(x)) #rounds to nearest integer (however it uses banker's rounding, so rounds .5 to an even int)

y= 3.14159265
print(round(y, 3)) #will round to 3dp

print(abs(-2.9)) #takes absolute value of number i.e., makes it positive

import math #imports the math module with allows you to use many more math methods
print(math.ceil(2.9)) #prints the ceil
print(math.floor(2.9)) #prints the floor
print(math.pi) #prints pi